package shihuo.serve;

public class Include {
	public Boolean include_0_100(float value){
		if (value > 0 && value < 100) {
			return true;
		}else {
			return false;
		}
	}
	public Boolean include_100_300(float value){
		if (value > 100 && value < 300) {
			return true;
		}else {
			return false;
		}
	}
	public Boolean include_300_500(float value){
		if (value > 300 && value < 500) {
			return true;
		}else {
			return false;
		}
	}
	public Boolean include_500_1000(float value){
		if (value > 500 && value < 1000) {
			return true;
		}else {
			return false;
		}
	}
	public Boolean include_1000_10000(float value){
		if (value > 1000 && value < 10000) {
			return true;
		}else {
			return false;
		}
	}
	public Boolean include_10000max(float value){
		if (value > 10000) {
			return true;
		}else {
			return false;
		}
	}
}
